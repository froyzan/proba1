#include "libft.h"

int	ft_isalnum(int c)
{
	if ((c >= 'A' && c <= 'Z') || (c >= 'a' && c <= 'z'))
	{
		return (4);
	}
	else if (c >= '0' && c <= '9')
	{
		return (4);
	}
	return (0);
}
